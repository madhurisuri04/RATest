drop table if exists ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna

truncate table ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna

insert into ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna
select distinct 
	PaymentYear, 
	HICN, 
	HCC, 
	Hierarchy = CAST(NULL as varchar(1)), 
	HeirHCC = CAST(NULL as varchar(10)), 
	PlanID,
	RAFactorType
into ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna
from 
	ProdSupport.dbo.tbl_GAP_All_Diags_RAPS_PartC_Aetna
where 
	HCC is not null 
	and IsAdded = 'Y'
Except
select distinct 
	PaymentYear, 
	HICN, 
	HCC, 
	NULL, 
	NULL,
	PlanID,
	RAFactorType
from 
	ProdSupport.dbo.tbl_GAP_All_Diags_RAPS_PartC_Aetna
where 
	HCC is not null 
	and IsAdded = 'N'


--Apply Heirarchy

UPDATE A
SET Hierarchy = 'L'
FROM ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna A
WHERE 
	EXISTS
    (SELECT 1
		from ProdSupport.dbo.tbl_GAP_All_Diags_RAPS_PartC_Aetna B
		JOIN HRPReporting.dbo.lk_Risk_Models_Hierarchy C
            ON A.HICN = B.HICN
               AND B.HCC = C.HCC_KEEP
               AND A.HCC = C.HCC_DROP
               AND A.PaymentYear = B.PaymentYear
               AND C.RA_FACTOR_TYPE = B.RAFactorType
        where IsAdded = 'N'
	)


UPDATE A
SET Hierarchy = 'L'
FROM ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna A
WHERE 
	EXISTS
    (SELECT 1
		from ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna B
		JOIN HRPReporting.dbo.lk_Risk_Models_Hierarchy C
            ON A.HICN = B.HICN
               AND B.HCC = C.HCC_KEEP
               AND A.HCC = C.HCC_DROP
               AND A.PaymentYear = B.PaymentYear
               AND C.RA_FACTOR_TYPE = B.RAFactorType
	)


UPDATE A
SET Hierarchy = 'H'
FROM ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna A
WHERE Hierarchy is null
AND	EXISTS
    (SELECT 1
		from ProdSupport.dbo.tbl_GAP_All_Diags_RAPS_PartC_Aetna B
		JOIN HRPReporting.dbo.lk_Risk_Models_Hierarchy C
            ON A.HICN = B.HICN
               AND A.HCC = C.HCC_KEEP
               AND B.HCC = C.HCC_DROP
               AND A.PaymentYear = B.PaymentYear
               AND C.RA_FACTOR_TYPE = B.RAFactorType
        where IsAdded = 'N'
	)


drop table if exists #HierHCC

select distinct a.PaymentYear, a.HICN, a.HCC, b.HCC HierHCC, RANK() over (partition by a.hicn, a.hcc order by b.hcc) rnk
into #HierHCC
from 
      ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna a
      join ProdSupport.dbo.tbl_GAP_All_Diags_RAPS_PartC_Aetna b
      on a.PaymentYear = b.PaymentYear
      and a.HICN = b.HICN
      join HRPReporting.dbo.lk_Risk_Models_Hierarchy C
      on a.HCC = c.HCC_KEEP
      and b.HCC = c.HCC_DROP
      and b.PaymentYear = c.Payment_Year
      and b.RAFactorType = c.RA_FACTOR_TYPE 
where 
      a.Hierarchy = 'H'


update a
set 
	a.HeirHCC = b.HierHCC
from ProdSupport.dbo.tbl_GAP_HCC_RAPS_PartC_Aetna a
join #HierHCC b
on a.HICN = b.HICN
and a.HCC = b.HCC
and a.PaymentYear = b.PaymentYear
and b.rnk = 1


