drop table if exists ProdSupport.dbo.Temp_SummaryMMR_Aetna
select 
	PaymentYear, 
	HICN, 
	PartCRAFTProjected RAFactorType,
	min(PartDRaftMMR) PartDRaftMMR, 
	max(PaymStart) PaymStart,
	max(PlanID) PlanID,
	count(distinct PaymStart) PaymentMonths 
into ProdSupport.dbo.Temp_SummaryMMR_Aetna
